function findConfidence(threshold) {
    let bestMatch = ROC_VALUES[ROC_VALUES.length-1];
    for(let i=0; i<ROC_VALUES.length; i++) {
        if(ROC_VALUES[i].threshold < threshold) {
            bestMatch = ROC_VALUES[i]
            break;
        }
    }

    //I'm not sure there's a particularly mathematically well-grounded way to
    //do this from threshold, TPR, FPR.
    //However, I think a reasonable shot at this is just using 1.0-TPR.
    return 1.0-bestMatch.tpr;
}

